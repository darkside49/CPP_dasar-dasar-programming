#include <iostream>
using namespace std;

int main()
{
	// increment dan decrement
	// preincrement dan postincrement

	int a = 5;
	int b = 5;

	// postincrement
	cout << a << endl;
	cout << a++ << endl;
	cout << a << endl << endl;

	// preincrement
	cout << b << endl;
	cout << ++b << endl;
	cout << b << endl;



	cin.get();
	return 0;
}



// ===== RECODE ===== //
int main(){
  // increment & decrement
  // preincrement & postincrement
  // pre = sebelum & post = sesudah
  // increment = menambah
  // decrement = mengurangi
  
  int a = 10;
  int b = 10;
  int c = 10;
  int d = 10;
  
  // postincrement
  cout << a << endl;
  cout << a++ << endl;
  cout << a << endl;
  
  // preincrement
  cout << b << endl;
  cout << ++b << endl;
  cout << b << endl;
  
  // postdecrement
  cout << c << endl;
  cout << c-- << endl;
  cout << c << endl;
  
  // predecrement
  cout << d << endl;
  cout << --d << endl;
  cout << d << endl;
  
  getch();
  return 0;
}



